package com.gooddog.domain;

//펫 VO
public class PetVO {

	// 펫 번호
	private int pet_no;
	// 펫 이름 
	private String pet_name;
	// 펫 사진
	private String pet_img;
	// 펫 성별
	private int pet_gender;
	// 펫 견종
	private String pet_spacies;
	// 펫 생일
	private String pet_birth;
	// 펫 인식번호
	private String pet_ident;
	
	public int getPet_no() {
		return pet_no;
	}
	public void setPet_no(int pet_no) {
		this.pet_no = pet_no;
	}
	public String getPet_name() {
		return pet_name;
	}
	public void setPet_name(String pet_name) {
		this.pet_name = pet_name;
	}
	public String getPet_img() {
		return pet_img;
	}
	public void setPet_img(String pet_img) {
		this.pet_img = pet_img;
	}
	public int getPet_gender() {
		return pet_gender;
	}
	public void setPet_gender(int pet_gender) {
		this.pet_gender = pet_gender;
	}
	public String getPet_spacies() {
		return pet_spacies;
	}
	public void setPet_spacies(String pet_spacies) {
		this.pet_spacies = pet_spacies;
	}
	public String getPet_birth() {
		return pet_birth;
	}
	public void setPet_birth(String pet_birth) {
		this.pet_birth = pet_birth;
	}
	public String getPet_ident() {
		return pet_ident;
	}
	public void setPet_ident(String pet_ident) {
		this.pet_ident = pet_ident;
	}
	
		
}
